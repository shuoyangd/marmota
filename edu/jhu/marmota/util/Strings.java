package edu.jhu.marmota.util;

public class Strings {

	private Strings() {
		
	}
	
	static public String consolidate(String[] tokens) {
		if (tokens.length == 0) {
			return "";
		}
		else {
			String res = tokens[0].toString();
			for (int i = 1; i < tokens.length; i++) {
				res += (" " + tokens[i].toString());
			}
			return res;
		}
	}
}
